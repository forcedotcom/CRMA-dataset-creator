package com.force.crma.olympus;

public class DatasetCreatorException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DatasetCreatorException() {
		
	}

	public DatasetCreatorException(String message) {
		super(message);
		
	}

	public DatasetCreatorException(Throwable cause) {
		super(cause);
		
	}

	public DatasetCreatorException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public DatasetCreatorException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
